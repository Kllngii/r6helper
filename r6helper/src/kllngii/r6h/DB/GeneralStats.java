package kllngii.r6h.DB;

public class GeneralStats extends Stats {
	int assists;
	int bulletsFired;
	int bulletsHit;
	int dbno;
	int deaths;
	int gadgetsDestroyed;
	int headshotKills;
	int kills;
	int meleeKills;
	int suicides;
	
	
	public int getAssists() {
		return assists;
	}
	public void setAssists(int assists) {
		this.assists = assists;
	}
	public int getBulletsFired() {
		return bulletsFired;
	}
	public void setBulletsFired(int bulletsFired) {
		this.bulletsFired = bulletsFired;
	}
	public int getBulletsHit() {
		return bulletsHit;
	}
	public void setBulletsHit(int bulletsHit) {
		this.bulletsHit = bulletsHit;
	}
	public int getDbno() {
		return dbno;
	}
	public void setDbno(int dbno) {
		this.dbno = dbno;
	}
	public int getDeaths() {
		return deaths;
	}
	public void setDeaths(int deaths) {
		this.deaths = deaths;
	}
	public int getGadgetsDestroyed() {
		return gadgetsDestroyed;
	}
	public void setGadgetsDestroyed(int gadgetsDestroyed) {
		this.gadgetsDestroyed = gadgetsDestroyed;
	}
	public int getHeadshotKills() {
		return headshotKills;
	}
	public void setHeadshotKills(int headshotKills) {
		this.headshotKills = headshotKills;
	}
	public int getKills() {
		return kills;
	}
	public void setKills(int kills) {
		this.kills = kills;
	}
	public int getMeleeKills() {
		return meleeKills;
	}
	public void setMeleeKills(int meleeKills) {
		this.meleeKills = meleeKills;
	}
	public int getSuicides() {
		return suicides;
	}
	public void setSuicides(int suicides) {
		this.suicides = suicides;
	}
}
